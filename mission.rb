  puts "Enter the initial coordinates of your rover ##{i}:"
  rover = Rover.new(gets.chomp, plateau)
  puts "Give rover ##{i} movement directions:"
  rover.follow_directions(gets.chomp)
  puts "your new coordinates are: " + rover.output_coordinates
end



